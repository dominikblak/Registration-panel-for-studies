  <h2 style="text-align: center;">Zarządzanie projektami</h2>
</br>
  <h3>Charakterystyka studiów: </h3>
 <h4><p>Studia mają na celu przygotowanie kadr przedsiębiorstw IT do sprawnego zarządzania projektami informatycznymi oraz przeprowadzenia analizy biznesowej architektury tworzonego oprogramowania. Przedstawienie najbardziej popularnych klasycznych i zwinnych metod zarządzania umożliwi optymalny wybór efektywnego sposobu prowadzenia projektu zintegrowanego ze środowiskiem działania przedsiębiorstwa i skalowalnego do wielkości i złożoności projektu. Studia będą obejmować zagadnienia klasycznego (PMBOK) i zwinnego (SCRUM) zarządzania projektami, procesów wytwarzania oprogramowania (w tym programowania ekstremalnego XP), wspomagania narzędziowego wytwarzania oprogramowania, zapewnienia jakości (ISTQB). Duży nacisk został położony na zrozumienie całego otoczenia związanego z prowadzenia wielu projektów dotyczy to zagadnień odwzorowania i integracji klasycznych oraz zwinnych metodyk zarządzania projektami, a także środowiska zarządzania programem i portfelem projektów. Drugim obszarem studiów są zagadnienia związane z zarządzaniem produktem oraz wysokopoziomową architekturą oprogramowania. Ten obszar jest wspierany poprzez modelowanie biznesowe bazujące na BPMN, architektury korporacyjne (SOA – architektury bazujące na usługach), zwinne metodyki zarządzania produktem, estymację oprogramowania oraz zarządzanie bezpieczeństwem informacji i poziomem usług.</p>

<p>W trakcie wykładów będzie stosowany opis systemów zarządzania w oparciu o architektury korporacyjne oraz modele przedstawione w Archimate i BPMN.</p>

<p>Program studiów obejmuje ponadto zarządzanie kompetencjami pracowniczymi, aspekty prawne działalności firm informatycznych oraz zarządzanie bezpieczeństwem informacji i poziomem usług.</p>

<p>Na studiach oceny będą wystawiane poprzez egzaminy i zaliczenia w oparciu o praktyczne wykonanie zadań. Praktyczne wykonywanie zadań będzie koncentrować się przykładowo na opracowaniu zasad prowadzenia konkretnego projektu informatycznego wyskalowanych do jego wielkości i złożoności.</p>

<p>Uczestnicy studiów uzyskają wiedzę pomocną przy zdawaniu egzaminów zewnętrznych: PMP z zakresu metodyk zarządzania projektami bazującymi na metodyce PMBOK, Scrum Master, Scrum Product Owner, ISTQB obejmującego procesy testowania oprogramowania oraz z zakresu modelowania biznesowego i tworzenia architektury korporacyjnej oprogramowania.</p>

<p>Program studiów przewiduje mocne powiązanie wykładów z warsztatami również w środowisku laboratoryjnym, na których będą wdrażane najlepsze praktyki, techniki i narzędzia. Wiele z tych rozwiązań będzie wspomagane poprzez odpowiednie systemy informatyczne.</p></h4></br></br>

<h3>Program studiów </h3>
<h4><p>Studia podyplomowe będą obejmować zagadnienia klasycznego i zwinnego zarządzania projektami, analizy biznesowej i budowy architektury korporacyjnej.</p>

<p>Poniżej podano listę przedmiotów objętych programem studiów oraz zamierzone cele.</p><ul class="fa-ul">
  <li><i class="fa-li fa fa-check-square"></i><b>Architektura przedsięwzięcia informatycznego</b>. Cele: Poznanie podstaw zarządzania projektami opierającego się na najpopularniejszej na świecie metodyce klasycznej PMBOK oraz uzyskanie umiejętności w zakresie warsztatu pracy kierownika projektu. Przygotowanie do zdania egzaminu PMP lub CAPM. W celu powiązania zarządzania projektami z technologiami informacyjnymi zastosowano podejście architektury korporacyjnej z wykorzystaniem narzędzi ArchiMate i BPMN.</li>
  <li><i class="fa-li fa fa-check-square"></i><b>Metodyki zwinne zarządzania projektami.</b> Cele: Poznanie podstaw zwinnego zarządzania projektami informatycznymi bazującego na najpopularniejszej na świecie metodyce zwinnej SCRUM.  Poznanie procesu rozwoju oprogramowania z uwzględnieniem podstaw zwinnego (XP – Programowanie Ekstremalne) wytwarzania oprogramowania.</li>
  <li><i class="fa-li fa fa-check-square"></i><b> Zespoły w zwinnych projektach.</b> Cele: Poznanie zasad tworzenia i rozwoju efektywnych zespołów działających w zwinnym otoczeniu projektowym (podatnym na zmiany). Zaznajomienie się z zagadnieniami budowy i rozwoju zespołu w metodyce Scrum.</li>
  <li><i class="fa-li fa fa-check-square"></i><b>Metodyki zwinne zarządzania produktem.</b> Cele: Poznanie zasad planowaniu rozwoju produktu (oprogramowania) bazującego na iteracjach (sprintach) oraz wydaniach. Zaznajomienie się z rolą właściciela produktu w metodyce Scrum w utrzymaniu rejestru produktowego i współpracy zespołem w planowaniu wydań.</li>
  <li><i class="fa-li fa fa-check-square"></i><b>Zarządzanie jakością oprogramowania.</b> Cele: Poznanie podstaw zarządzania jakością oprogramowania; Uzyskanie wybranych umiejętności w zakresie warsztatu pracy testera i kierownika testów; Przygotowanie do zdania egzaminów na certyfikowanego testera ISTQB poziomu podstawowego i zaawansowanego.</li>
  <li><i class="fa-li fa fa-check-square"></i><b>Modelowanie architektury przedsiębiorstwa w ArchiMate.</b> Cele: Modelowanie architektury przedsiębiorstwa w sposób zrozumiały i przyjazny dla szerokiego spektrum osób w obszarze zarządzania i technologii informacyjnych. Uzyskanie umiejętności w zakresie podstaw modelowania w ArchiMate 2.0i.</li>
  <li><i class="fa-li fa fa-check-square"></i><b>Modelowanie biznesowe.</b> Cele: Poznanie podstaw modelowania biznesowego; Uzyskanie umiejętności w zakresie warsztatu pracy analityka biznesowego; Nabycie umiejętności modelowania w BPMN przy zastosowaniu odpowiednich narzędzi.</li>
  <li><i class="fa-li fa fa-check-square"></i><b>Zarządzanie projektami w przedsiębiorstwie informatycznym.</b> Cele: Poznanie podstaw zarządzania projektami w przedsiębiorstwie informatycznym, bazujących na metodach klasycznych i zwinnych; Poznanie praktycznych zasad zarządzania programami i portfelami projektów w oparciu o standardy PMI; Uzyskanie wybranych umiejętności w zakresie warsztatu pracy kierownika programu w oparciu o wymagania certyfikatu PgMP.</li>
  <li><i class="fa-li fa fa-check-square"></i><b> Architektura korporacyjna.</b> Cele: Wprowadzenie do modelowania struktury, funkcji i wzajemnych powiązań przedsiębiorstw, poznanie standardów opisów architektury korporacyjnej, przedstawienie zasad tworzenia architektury korporacyjnej z wykorzystaniem paradygmatu SOA.</i></li>
</ul></h4></br></br>

