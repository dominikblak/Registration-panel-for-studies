  <h3>Charakterystyka studiów: </h3>
 <h4><p>Studia dają możliwość zapoznania się z problemami i technologiami realizacji systemów baz danych oraz nabycia podstawowych umiejętności z zakresu projektowania, tworzenia i eksploatacji takich systemów, realizowanych w oparciu o najnowsze technologie. W programie studiów szczególną uwagę zwrócono na problemy tworzenia logicznych modeli baz danych, a także aplikacji charakterystycznych dla wielopoziomowych architektur, wykorzystujących techniki webowe pozwalające na tworzenie aplikacji dostępnych poprzez WWW. Tematyka studiów dotyczy też problemów administracji i bezpieczeństwa tych systemów, z uwzględnieniem uwarunkowań prawnych ich wdrażania i eksploatacji. Powyższą tematykę uzupełniają technologie dotyczące budowy hurtowni danych, realizacji procesów eksploracji danych i zarządzania wiedzą</p></h4></br></br>

<h3>Program studiów </h3>
<h4><ul class="fa-ul">
	<li><i class="fa-li fa fa-check-square"></i>Szczególna uwaga zwrócona jest na techniki realizacji nowoczesnych systemów o zróżnicowanych modelach logicznych, a także wielopoziomowych architekturach (klient-serwer), wykorzystujących środowisko sieciowe i techniki webowe, pozwalające na tworzenie aplikacji dostępnych poprzez WWW.</li>
	<li><i class="fa-li fa fa-check-square"></i>Istotnymi zagadnieniami, które znalazły swoje miejsce w technologii baz danych, są sposoby wykorzystywania olbrzymich ilości zgromadzonych danych, tak dla ich analizy (hurtownie danych i systemy OLAP), jak i udostępniania ich poprzez portale internetowe.</li>
	<li><i class="fa-li fa fa-check-square"></i>Ciekawym akcentem programu studiów jest prezentacja możliwości zarządzania wiedzą poprzez generowanie z baz danych odpowiadających im baz wiedzy (eksploracja baz danych) oraz poznanie zasad budowy baz wiedzy i inteligentnych aplikacji wnioskujących w oparciu o te modele wiedzy (podstawy sztucznej inteligencji i systemów ekspertowych)</li>
	<li><i class="fa-li fa fa-check-square"></i>W programie studiów istotne miejsce zajmują problemy zapewnienia bezpieczeństwa tworzonym i eksploatowanym systemom baz danych jako całości, a także szeroko pojęte bezpieczeństwo samych danych (często wymagane ustawowo).</li>
	<li><i class="fa-li fa fa-check-square"></i>Nowość programu stanowią zagadnienia związane z teorią i praktyką analizy mediów społecznościowych, takich jak: blogosfera, Twitter czy YouTube przy pomocy najnowszych metod dedykowanych analizie sieci społecznościowych.</li></ul>
	Program uwzględnia problemy związane z zarządzaniem przedsięwzięciami projektowymi, a także aspekty prawne wdrażania i eksploatacji systemów baz danych</h4></br></br>

<h3>Sylwetka absolwenta: </h3>

<h4><p>Studia skierowane są do tych wszystkich, którzy będą tworzyli koncepcje, projektowali, implementowali, eksploatowali lub nadzorowali prace nad systemami realizowanymi w szeroko pojętej technologii baz danych, a także do osób zainteresowanych nowoczesnymi systemami informacyjnymi i kierunkami ich rozwoju.</p>

<p>W stopniu zależnym od poziomu wiedzy początkowej słuchacza (nie są wymagane studia informatyczne), absolwent posiądzie umiejętności projektowania, implementowania niewielkich systemów relacyjnych baz danych oraz administrowania nimi. Po opanowaniu podstawowych zasad posługiwania się podstawowymi aplikacjami środowiska sieciowego nabędzie on umiejętność korzystania i tworzenia aplikacji baz danych z wykorzystaniem nowoczesnych technologii webowych wykorzystujących środowisko Internetu. Absolwent pozna też zasady tworzenia aplikacji opartych o technologie sztucznej inteligencji w postaci systemów ekspertowych, wykorzystujących bazy wiedzy czy sieci neuronowe. Dodatkowo pozna wiele elementów nowoczesnych technik bazodanowych (modele nierelacyjne, zasady hurtowni danych, podstawy eksploracji danych), czy też technik sieciowych. Dopełnieniem powyższej wiedzy będą elementy szeroko pojętej inżynierii systemów baz danych obejmującej zagadnienia bezpieczeństwa tych systemów, poprawnego nimi administrowania, zarządzania projektami, zarządzania jakością systemów czy wreszcie najistotniejsze zagadnienia prawne związane z procesem tworzenia i eksploatacją tych systemów.</p>

<p>Uczestnicy po zaliczeniu wszystkich przedmiotów objętych planem studiów (w formie przewidzianej indywidualnie dla każdego z przedmiotów) uzyskują Świadectwo ukończenia studiów podyplomowych z zakresu Systemów Baz Danych, wystawiane przez Akademię Górniczo-Hutniczą, zgodne z wymogami Ministerstwa Nauki i Szkolnictwa Wyższego. Nabyta wiedza i umiejętności są bardzo dobrą podstawą do dalszych szczegółowych studiów i kursów w tym zakresie, czy podjęcia starań o przystąpienie do wybranych egzaminów certyfikacyjnych, pozwalających uzyskać np. wybrany Europejski Certyfikat Umiejętności Komputerowych – ECDL</p></h4></br></br>
 

